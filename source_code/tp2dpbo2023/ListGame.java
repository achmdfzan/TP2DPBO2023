    public void setPanel() {
        mainPanel.removeAll();
        mainPanel.setLayout(new GridLayout(0, 1));
        
        ResultSet res = db.selectQuery("SELECT * FROM game WHERE dev_id = " + Session.getDevID());
        try {
            while(res.next()) {
                GameCard curr = new GameCard(this, res.getInt("game_id"), res.getInt("dev_id"), res.getString("nama"), res.getString("genre"), res.getString("platform"), res.getString("image"));
                
                mainPanel.add(curr);
            }
        } catch (SQLException ex) {
            System.out.println(ex.getMessage());
        }
        
        mainPanel.revalidate();
        mainPanel.repaint();
    }

